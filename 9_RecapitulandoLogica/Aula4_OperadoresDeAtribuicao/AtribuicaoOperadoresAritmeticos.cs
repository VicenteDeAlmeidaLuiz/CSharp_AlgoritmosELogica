using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AlgoritmosELogicaCsharp.RecapitulandoLogica.Aula4_OperadoresDeAtribuicao
{
    internal class AtribuicaoOperadoresAritmeticos
    {
        static void Main(string[] args)
        {
            /*Com o ++ eu incremento o valor,adiciono mais 1*/

            int a = 90;
            Console.WriteLine(a); /*saida 90*/
            a++;
            Console.WriteLine(a); /*saida 91*/

            Console.WriteLine("____________________");

            /*Com o -- eu decremento o valor,subtraio 1*/

            int b = 100;
            Console.WriteLine(b); /*Saída 100*/
            b--;
            Console.WriteLine(b); /*Saída 99*/



        }
    }
}
