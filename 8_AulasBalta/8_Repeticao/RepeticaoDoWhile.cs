using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AlgoritmosELogicaCsharp.AulasBalta._8_Repeticao
{
    internal class RepeticaoDoWhile
    {
        static void Main(string[] args)
        {

            var valor = 0;

            do
            {
                Console.WriteLine("Teste");
                valor++;
            } while (valor <= 5);



        }
    }
}

