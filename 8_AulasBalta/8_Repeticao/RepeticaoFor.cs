using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AlgoritmosELogicaCsharp.AulasBalta._8_Repeticao
{
    internal class RepeticaoFor
    {
        static void Main(string[] args)
        {
            /*Conta de zero a cinco*/
            for (int i = 0; i <= 5; i++)
            {
                Console.WriteLine(i);



            }
            Console.WriteLine("________________________________");
            /*Conta de 1 a 5*/

            for (int i = 1; i <= 5; i++)
            {
                Console.WriteLine(i);



            }
            Console.WriteLine("________________________________");
            /*Conta de 0 a 4*/

            for (int i = 0; i <= 4; i++)
            {
                Console.WriteLine(i);



            }
            Console.WriteLine("________________________________");
            for (int i = 5; i >= 0; i--)
            {
                Console.WriteLine(i);


            }

        }
    }
}
